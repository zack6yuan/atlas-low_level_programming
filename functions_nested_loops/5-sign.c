/**
* print_sign - prints the sign of a number
* @c: the number that is being checked
* Return: 1 and + for > 0, 0 and 0 for = 0, and -1 and - for < 0
*/
int print_sign(int n);
{
    if (n > 0)
    {
    return (1);
    }
    else if (n = 0)
    {
    return (0);
    }
    else
    {
    return (-1);
    }
}