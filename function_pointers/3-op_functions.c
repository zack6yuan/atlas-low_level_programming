#include "3-calc.h"
/**
 * op_add - returns the sum of a + b
 * @a: the first number being added
 * @b: the second number being added
 * Return: value of added numbers
 */
int op_add(int a, int b);
{
return (a + b);
}
/**
 * op_sub - returns the difference of a - b
 * @a: number that will be subtracted from
 * @b: tnumber subtracted from the first number
 * Return: value of subtracted numbers
 */
int op_sub(int a, int b);
{
return (a - b);
}
/**
 * op_mul - returns the multiplication of a * b
 * @a: first number that is being multiplied
 * @b: second number that is beign multiplied
 * Return: value of multiplied numbers
 */
int op_mul(int a, int b);
{
return (a * b);
}
/**
 * op_div - returns the divison of a % b
 * @a: number that will be divided
 * @b: number that the first number will be divided by
 * Return: value of divided numbers
*/
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - returns the remainder of division of a / b
 * @a: the number that will be divided
 * @b: number that the first number will be divided by
 * Return: the remainder or modulo
 */
int op_mod(int a, int b)
{
return (a % b);
}